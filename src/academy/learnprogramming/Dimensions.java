package academy.learnprogramming;

public class Dimensions {
    private int widths;
    private int heights;
    private int depth;

    public Dimensions(int widths, int heights, int depth) {
        this.widths = widths;
        this.heights = heights;
        this.depth = depth;
    }

    public int getWidths() {
        return widths;
    }

    public int getHeights() {
        return heights;
    }

    public int getDepth() {
        return depth;
    }
}
